  /// Get today's verse based on day of year
  static Map<String, dynamic> getTodaysVerse() {
    final dayOfYear = DateTime.now().difference(DateTime(DateTime.now().year)).inDays;
    return dailyVerses[dayOfYear % dailyVerses.length];
  }

  /// Get today's hadith based on day of year
  static Map<String, dynamic> getTodaysHadith() {
    final dayOfYear = DateTime.now().difference(DateTime(DateTime.now().year)).inDays;
    return dailyHadith[dayOfYear % dailyHadith.length];
  }

  /// Get appropriate dua for current time
  static Map<String, dynamic> getCurrentDua() {
    final hour = DateTime.now().hour;
    
    if (hour >= 5 && hour < 12) {
      return dailyDuas.firstWhere((dua) => dua['occasion'] == 'Morning');
    } else if (hour >= 17 && hour < 20) {
      return dailyDuas.firstWhere((dua) => dua['occasion'] == 'Evening');
    } else {
      // Return a general dua
      return dailyDuas.firstWhere((dua) => dua['occasion'] == 'For Guidance');
    }
  }

